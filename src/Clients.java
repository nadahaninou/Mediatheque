    private void rechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rechActionPerformed

        tablemodele.setRowCount(0); // Clear the table before populating it

        String a = isbn.getText();
        isbn.setText("");
        GestionClient gest = new GestionClient();
        LinkedList<Client> livres = new LinkedList<>(); // Initialize livres as an empty list

        switch (combchoix.getSelectedItem().toString()) {
            case "matriculeClient":
            
            {
                try {
                    livres = gest.getclientByMatriculeClient(a);
                } catch (SQLException ex) {
                    Logger.getLogger(Clients.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
           
            break;

            case "nom":
            
            {
                try {
                    livres = gest.getclientByNom(a);
                } catch (SQLException ex) {
                    Logger.getLogger(Clients.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
            break;

            case "prenom":
            try {
                livres = gest.getclientByPrenom(a);
            } catch (SQLException ex) {
                Logger.getLogger(Clients.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
            case "typeAbonnement":
            try {
                livres = gest.getclientByTypeAbonnement(a);
            } catch (SQLException ex) {
                Logger.getLogger(Clients.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
           
        } 
        

        for (Client livre : livres) {
            Object[] rowData = {
                livre.getMatriculeClient(),
                livre.getNom(),
                livre.getPrenom(),
                livre.getTypeAbonnement(),
                
            };
            tablemodele.addRow(rowData);
        }
    }//GEN-LAST:event_rechActionPerformed

