    private void ValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderActionPerformed
        switch(choix.getSelectedItem().toString()){
            case "Ajouter Emprunt" +
"":
            {
                AddEmprunt frameXe = new AddEmprunt();
                frameXe.setVisible(true);
                tableLivre.setVisible(false);

            }
            break;

            case "Supprimer Emprunt": {
                isbn.setVisible(true);
                labisn.setVisible(true);
                isbn1.setVisible(true);
                labisn1.setVisible(true);
                supp.setVisible(true);
                combchoix.setVisible(false);

                rech.setVisible(false);
                tableLivre.setVisible(false);
            }
            break;

            case "Rechercher Emprunt":{

                labisn.setVisible(false);
                labisn1.setVisible(false);
                supp.setVisible(false);
                combchoix.setVisible(true);
                isbn.setVisible(true);
                isbn1.setVisible(false);
                rech.setVisible(true);
                tableLivre.setVisible(true);

            }
            break;
            case "Afficher tous  les Emprunts":{
                tableLivre.setVisible(true);
                isbn.setVisible(false);
                labisn.setVisible(false);
                isbn1.setVisible(false);
                labisn1.setVisible(false);
                supp.setVisible(false);
                combchoix.setVisible(false);

                rech.setVisible(false);
                tablemodele.setRowCount(0);
                GestionEmprunts gest = new GestionEmprunts();
                LinkedList<Emprunt> livres = new LinkedList<>();
               
            try {
                livres = gest.afficherTousLesEmprunts();
            } catch (SQLException ex) {
                Logger.getLogger(Emprunte.class.getName()).log(Level.SEVERE, null, ex);
            }
                
                for (Emprunt livre : livres) {
                    Object[] rowData = {
                        livre.getMatriculeClient(),
                        livre.getIdKindles(),
                        livre.getDateEmprunt(),
                        
                    };
                    tablemodele.addRow(rowData);

                }
                break;}
        }
    }//GEN-LAST:event_ValiderActionPerformed

