    private void rechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rechActionPerformed

        tablemodele.setRowCount(0); // Clear the table before populating it

        String a = isbn.getText();
        isbn.setText("");
        GestionEmprunts gest = new GestionEmprunts();
        LinkedList<Emprunt> livres = new LinkedList<>(); // Initialize livres as an empty list

        switch (combchoix.getSelectedItem().toString()) {
            case "idkindles":

            {
              
            try {
                livres = gest.getEmpruntsByIdKindles(a);
            } catch (SQLException ex) {
                Logger.getLogger(Emprunte.class.getName()).log(Level.SEVERE, null, ex);
            }
                
            }
            break;

            case "matricule":

            {
                
            try {
                livres = gest.getEmpruntsByMatriculeClient(a);
            } catch (SQLException ex) {
                Logger.getLogger(Emprunte.class.getName()).log(Level.SEVERE, null, ex);
            }
                
                    
            }

            break;

            

        }

        for (Emprunt livre : livres) {
            Object[] rowData = {
                livre.getMatriculeClient(),
                livre.getIdKindles(),
                livre.getDateEmprunt(),

            };
            tablemodele.addRow(rowData);
        }
    }//GEN-LAST:event_rechActionPerformed

