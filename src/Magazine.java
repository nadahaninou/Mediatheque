    private void rechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rechActionPerformed

        tablemodele.setRowCount(0); // Clear the table before populating it

        String a = isbn.getText();
        isbn.setText("");
        GestionMagazin gest = new GestionMagazin();
        LinkedList<Magazin> livres = new LinkedList<>(); // Initialize livres as an empty list

        switch (combchoix.getSelectedItem().toString()) {
            case "frequenceDePublication":
            try {
                livres = gest.getArticleByFrequenceDePublication(a);
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
            case "genre":
            try {
                livres = gest.getArticleByGenre(a);
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
            case "numeroDeMgazin":
            try {
                livres = gest.getArticleByNumeroDeMgazin(Integer.parseInt(a));
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
            case "ISBN":
            try {
                livres = gest.getMagazinsByISBN(a);
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
            case "titre":
            try {
                livres = gest.getMagazinsByTitre(a);
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
            case "Editeur":
            try {
                livres = gest.getMagazinsByEditeur(a);
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
            case "anneeEdition":
            try {
                livres = gest.getMagazinsByAnneeEdition(Integer.parseInt(a));
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
            case "URL":
            try {
                livres = gest.getMagazinsByURL(a);
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
            case "favorie":
            try {
                livres = gest.getMagazinsByFavorie(Integer.parseInt(a));
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;
            case "recommande":
            try {
                livres = gest.getMagazinsByRecommande(Integer.parseInt(a));
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;

            default:
            break;
        }

        for (Magazin livre : livres) {
            Object[] rowData = {
                livre.getFrequenceDePublication(),
                livre.getGenre(),
                livre.getNumeroDeMgazin(),
                livre.getISBN(),
                livre.getTitre(),
                livre.getEditeur(),
                livre.getAnneeEdition(),
                livre.getURL(),
                livre.getFavorie(),
                livre.getRecommande(),
                livre.getAuteur1(),
                livre.getAuteur2(),
                livre.getAuteur3(),
                livre.getAuteur4(),
                livre.getAuteur5()
            };
            tablemodele.addRow(rowData);
        }

    }//GEN-LAST:event_rechActionPerformed

