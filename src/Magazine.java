    private void ValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderActionPerformed
        switch(choix.getSelectedItem().toString()){
            case "Ajouter Magazin":
            {
                AddMagazin frameXe = new AddMagazin();
                frameXe.setVisible(true);
                tableLivre.setVisible(false);

            }
            break;

            case "Supprimer Magazin": {
                isbn.setVisible(true);
                labisn.setVisible(true);
                supp.setVisible(true);
                combchoix.setVisible(false);

                rech.setVisible(false);
                tableLivre.setVisible(false);
            }
            break;

            case "Rechercher Magazin":{

                labisn.setVisible(false);
                supp.setVisible(false);
                combchoix.setVisible(true);
                isbn.setVisible(true);
                rech.setVisible(true);
                tableLivre.setVisible(true);

            }
            break;
            case "Afficher tous Magazin":{
                tableLivre.setVisible(true);
                isbn.setVisible(false);
                labisn.setVisible(false);
                supp.setVisible(false);
                combchoix.setVisible(false);

                rech.setVisible(false);
                tablemodele.setRowCount(0);
                GestionMagazin gest = new GestionMagazin();
                LinkedList<Magazin> livres = new LinkedList<>();
            try {
                livres = gest.afficherTousLesMagazins();
            } catch (SQLException ex) {
                Logger.getLogger(Magazine.class.getName()).log(Level.SEVERE, null, ex);
            }
                for (Magazin livre : livres) {
                    Object[] rowData = {
                        livre.getFrequenceDePublication(),
                        livre.getGenre(),
                        livre.getNumeroDeMgazin(),
                        livre.getISBN(),
                        livre.getTitre(),
                        livre.getEditeur(),
                        livre.getAnneeEdition(),
                        livre.getURL(),
                        livre.getFavorie(),
                        livre.getRecommande(),
                        livre.getAuteur1(),
                        livre.getAuteur2(),
                        livre.getAuteur3(),
                        livre.getAuteur4(),
                        livre.getAuteur5()
                    };
                    tablemodele.addRow(rowData);

                }
                break;}
        }

    }//GEN-LAST:event_ValiderActionPerformed

