    private void rechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rechActionPerformed
        tablemodele.setRowCount(0); // Clear the table before populating it

        String a = isbn.getText();
        isbn.setText("");
        GestionRoman gest = new GestionRoman();
        LinkedList<Roman> livres = new LinkedList<>(); // Initialize livres as an empty list

        switch (combchoix.getSelectedItem().toString()) {
            case "genre":
            {
                try {
                    livres = gest.getRomanByGenre(a);
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Romane.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            break;

        case "nbChapitres":
            {
                try {
                    livres = gest.getRomansByNBChapitres(Integer.parseInt(a));
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Romane.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            break;

            case "ISBN":
            {
                try {
                    livres = gest.getRomansByISBN(a);
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Romane.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            break;

            case "titre":
            {  
                try {
                    livres = gest.getRomansByTitre(a);
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Romane.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
            break;

            case "Editeur":
             try {
                    livres = gest.getRomansByEditeur(a);
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Romane.class.getName()).log(Level.SEVERE, null, ex);
                }
            break;
            case "anneeEdition":
            try {
                    livres = gest.getRomansByAnneeEdition(Integer.parseInt(a));
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Romane.class.getName()).log(Level.SEVERE, null, ex);
                }
            break;
            case "URL":
           try {
                    livres = gest.getRomansByURL(a);
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Romane.class.getName()).log(Level.SEVERE, null, ex);
                }
            break;
            case "favorie":
            try {
                    livres = gest.getRomansByFavorie(Integer.parseInt(a));
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Romane.class.getName()).log(Level.SEVERE, null, ex);
                }
            break;
            case "recommande":
            try {
                livres = gest.getRomansByRecommande(Integer.parseInt(a));
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(Romane.class.getName()).log(Level.SEVERE, null, ex);
            }
            break;

            default:
            break;
        }

        for (Roman livre : livres) {
            Object[] rowData = {
                livre.getGenre(),
                livre.getNbChapitres(),
                livre.getISBN(),
                livre.getTitre(),
                livre.getEditeur(),
                livre.getAnneeEdition(),
                livre.getURL(),
                livre.getFavorie(),
                livre.getRecommande(),
                livre.getAuteur1(),
                livre.getAuteur2(),
                livre.getAuteur3(),
                livre.getAuteur4(),
                livre.getAuteur5()
            };
            tablemodele.addRow(rowData);
        }
    }//GEN-LAST:event_rechActionPerformed

