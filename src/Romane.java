    private void ValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderActionPerformed
        tablemodele.setRowCount(0);
        String a = isbn.getText();
        isbn.setText("");
        GestionRoman gest = new GestionRoman();
        LinkedList<Roman> articles = new LinkedList<>();
        switch(choix.getSelectedItem().toString()){
            case "Ajouter Roman":
            {
                AddRoman frameXe = new AddRoman();
                frameXe.setVisible(true);
                tableLivre.setVisible(false);

            }
            break;

            case "Supprimer Roman": {
                isbn.setVisible(true);
                labisn.setVisible(true);
                supp.setVisible(true);
                combchoix.setVisible(false);

                rech.setVisible(false);
                tableLivre.setVisible(false);
            }
            break;

            case "Rechercher Roman":{

                labisn.setVisible(false);
                supp.setVisible(false);
                combchoix.setVisible(true);
                isbn.setVisible(true);
                rech.setVisible(true);
                tableLivre.setVisible(true);

            }
            break;
            case "Afficher tous les Romans":{
                tableLivre.setVisible(true);
                isbn.setVisible(false);
                labisn.setVisible(false);
                supp.setVisible(false);
                combchoix.setVisible(false);

                rech.setVisible(false);
                tablemodele.setRowCount(0);
                GestionRoman geste = new GestionRoman();
                LinkedList<Roman> articless = new LinkedList<>();
            try {
                articless = geste.afficherTousLesRomans();
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(Romane.class.getName()).log(Level.SEVERE, null, ex);
            }
                for (Roman livre : articless) {
                    Object[] rowData = {
                        livre.getGenre(),
                        livre.getNbChapitres(),
                        livre.getISBN(),
                        livre.getTitre(),
                        livre.getEditeur(),
                        livre.getAnneeEdition(),
                        livre.getURL(),
                        livre.getFavorie(),
                        livre.getRecommande(),
                        livre.getAuteur1(),
                        livre.getAuteur2(),
                        livre.getAuteur3(),
                        livre.getAuteur4(),
                        livre.getAuteur5()
                    };
                    tablemodele.addRow(rowData);

                }
                break;}}
    }//GEN-LAST:event_ValiderActionPerformed

